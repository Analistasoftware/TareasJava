package com.constructores;



public class Persona {
	
	//Atributos
  private int edad;
  private String nombre;
  
  //Constructor
 public Persona() {
	
}
  
  //Metodos Get y Set
  public int getEdad() {
	return edad;
}

public void setEdad(int edad) {
	this.edad = edad;
}

public String getNombre() {
	return nombre;
}

public void setNombre(String nombre) {
	this.nombre = nombre;
}


  

}
